pub fn print_integer_max_values() {
    println!("{:?}", i8::max_value());
    println!("{:?}", i8::min_value());
    println!("{:?}", i16::max_value());
    println!("{:?}", i16::min_value());
    println!("{:?}", i32::max_value());
    println!("{:?}", i32::min_value());
    println!("{:?}", i64::max_value());
    println!("{:?}", i64::min_value());
    println!("{:?}", i128::max_value());
    println!("{:?}", i128::max_value());
}